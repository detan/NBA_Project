             z.FTA=mean(data[(frist:final), ]$z.FTA),
             z.TRB=mean(data[(frist:final), ]$z.TRB),
             z.AST=mean(data[(frist:final), ]$z.AST),
             z.STL=mean(data[(frist:final), ]$z.STL),
             z.BLK=mean(data[(frist:final), ]$z.BLK),
             z.TOV=mean(data[(frist:final), ]$z.TOV),
             z.PF=mean(data[(frist:final), ]$z.PF),
             diffEFG=mean(data[(frist:final), ]$diffEFG),
             diffTOV=mean(data[(frist:final), ]$diffTOV),
             diffRB=mean(data[(frist:final), ]$diffRB),
             diffFTR=mean(data[(frist:final), ]$diffFTR),
             EFGP=mean(data[(frist:final), ]$EFGP),
             TOVP=mean(data[(frist:final), ]$TOVP),
             ORBP=mean(data[(frist:final), ]$ORBP),
